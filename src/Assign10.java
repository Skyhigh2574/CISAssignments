public class Assign10 {

        // Method to print a message
        void printMessage(String message) {
            System.out.println(message);
        }

    // Main class that contains the main method
    public class Main {
        public static void main(String[] args) {
            // Create an instance of the MessagePrinter class
            Assign10 messagePrinter = new Assign10();

            // Call the printMessage method with a message
            messagePrinter.printMessage("Hello, World!");
        }
    }
}
